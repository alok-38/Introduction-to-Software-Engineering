# Ask the user for two numbers and do math with them
number1 = float(input("Enter your first number: "))
number2 = float(input("Enter your second number: "))

print(f"{number1} + {number2} = {number1 + number2}")
print(f"{number1} - {number2} = {number1 - number2}")
print(f"{number1} * {number2} = {number1 * number2}")
print(f"{number1} / {number2} = {number1 / number2}")
